package day1;
/*2. Используя while вывести все числа от 0 до 25 в одну строку через пробел.*/

public class Task2_2 {
    public static void main(String[] args) {
        int number = 0;
        while (number <= 25) {
            System.out.print (number + " ");
            number++;
        }
    }
}
